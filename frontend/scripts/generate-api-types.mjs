/**
 * 从后端 FastAPI 获取 OpenAPI schema，生成 TypeScript 类型。
 *
 * 使用方式：
 *   1. 确保后端已在 http://localhost:8000 运行
 *   2. npm run generate:api
 */
import { writeFile } from 'node:fs/promises'
import ts from 'typescript'
import openapiTS from 'openapi-typescript'

const BACKEND_URL = process.env.BACKEND_URL || 'http://localhost:8000'
const OUTPUT = new URL('../src/services/api/generated.d.ts', import.meta.url)

async function fetchSchema() {
  const res = await fetch(`${BACKEND_URL}/openapi.json`)
  if (!res.ok) throw new Error(`Failed to fetch schema: ${res.statusText}`)
  return res.json()
}

async function generate() {
  console.log(`Fetching OpenAPI schema from ${BACKEND_URL}/openapi.json ...`)
  const schema = await fetchSchema()

  console.log('Generating TypeScript types ...')
  const nodes = await openapiTS(schema, {
    commentHeader: '/* eslint-disable */\n// Auto-generated by openapi-typescript. Do not edit.\n'
  })

  const printer = ts.createPrinter({ newLine: ts.NewLineKind.LineFeed })
  const sf = ts.createSourceFile('output.d.ts', '', ts.ScriptTarget.Latest, false, ts.ScriptKind.TS)
  const output = nodes.map((n) => printer.printNode(ts.EmitHint.Unspecified, n, sf)).join('\n')

  await writeFile(OUTPUT, output, 'utf-8')
  console.log(`Types written to ${OUTPUT}`)
}

generate().catch((err) => {
  console.error(err)
  process.exit(1)
})
